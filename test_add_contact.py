# -*- coding: utf-8 -*-
from selenium import webdriver
from selenium.webdriver.support.ui import Select
from selenium.common.exceptions import NoSuchElementException
from selenium.common.exceptions import NoAlertPresentException
import unittest
from properties import Properties
from addition_properties import Addition

class TestAddContact(unittest.TestCase):
    def setUp(self):
        self.wd = webdriver.Firefox()
        self.wd.implicitly_wait(30)

    # test-case one
    def test_add_contact(self):
        wd = self.wd
        self.open_home_page(wd)
        self.login(wd, username="admin", password="secret")
        self.open_page_for_create_new_contact(wd)
        self.filling_form_to_contact(wd, Properties(firstname="QQQ", middlename="WWW", lastname="EEE",nickname="SSS",
                                                    company="CCC",address="GGG", title="111", home="zzz",mobile="VVV",
                                                    email="TTT", work="9371992", fax="123"),
                                     Addition(email2="eee", email3="PPP",address2= "ddd",phone2="123",notes="eee"))


        self.zapolnenie_bday(wd)
        self.zapolnenie_aday(wd)
        self.zapolnenie_dop_poley(wd, Addition(address2="BAZOQUA", phone2="79996311111", notes="законы просты",
                                               email2= "spenc", email3= "ssss"))
        self.sumbit_contact(wd)
        self.logout(wd)

    def logout(self, wd):
        wd.find_element_by_link_text("Logout").click()

    def sumbit_contact(self, wd):
        wd.find_element_by_xpath("(//input[@name='submit'])[2]").click()

    def zapolnenie_dop_poley(self, wd, addition_properties):
        wd.find_element_by_name("address2").click()
        wd.find_element_by_name("address2").clear()
        wd.find_element_by_name("address2").send_keys("%s" % addition_properties.address2)
        wd.find_element_by_name("phone2").click()
        wd.find_element_by_name("phone2").clear()
        wd.find_element_by_name("phone2").send_keys("%s" % addition_properties.phone2)
        wd.find_element_by_name("notes").click()
        wd.find_element_by_name("notes").clear()
        wd.find_element_by_name("notes").send_keys("%s" % addition_properties.notes)

    def zapolnenie_aday(self, wd):
        wd.find_element_by_name("aday").click()
        Select(wd.find_element_by_name("aday")).select_by_visible_text("12")
        wd.find_element_by_xpath("(//option[@value='12'])[2]").click()
        wd.find_element_by_name("amonth").click()
        Select(wd.find_element_by_name("amonth")).select_by_visible_text("May")
        wd.find_element_by_xpath("(//option[@value='May'])[2]").click()
        wd.find_element_by_name("ayear").click()
        wd.find_element_by_name("ayear").clear()
        wd.find_element_by_name("ayear").send_keys("2009")

    def zapolnenie_bday(self, wd):
        wd.find_element_by_name("bday").click()
        # select date of drop-list
        Select(wd.find_element_by_name("bday")).select_by_visible_text("5")
        # transfer value "5"
        wd.find_element_by_xpath("//option[@value='5']").click()
        wd.find_element_by_name("bmonth").click()
        Select(wd.find_element_by_name("bmonth")).select_by_visible_text("October")
        wd.find_element_by_xpath("//option[@value='October']").click()
        wd.find_element_by_name("byear").click()
        wd.find_element_by_name("byear").clear()
        wd.find_element_by_name("byear").send_keys("2008")

